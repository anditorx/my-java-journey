public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 20;
        int b = 5;
        int harga = 20000;
        int jumlahBeli = 3;
        int totalHarga = harga * jumlahBeli;
        System.out.println(totalHarga);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
